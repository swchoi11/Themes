class Prompt:
    @staticmethod
    def category():
        return """
        이슈 분류와 예시는 다음과 같습니다. 

         <이슈와 그 예시>
         * alignment
           - 화면요소들(아이콘, 텍스트, 버튼 등)이 일관된 정렬 기준을 따르지 않음
           - 컴포넌트 내부 요소들의 수직/수평 정렬이 균일하지 않음
           - 동일 계층 요소들 간의 정렬 기준점이 서로 다름
         * cut off
           - 콘텐츠나 ui요소가 화면 또는 컨테이너 경계를 벗어나 일부 잘림
           - 텍스트가 할당된 영역을 초과하여 영역 외 텍스트 잘림
           - 이미지나 아이콘이 지정된 영역보다 커서 가장자리가 보이지 않음
         * not fit
           - 콘텐츠와 화면 비율이 맞지 않아 불필요한 여백이 많이 발생함
           - 화면 방향(가로/세로) 전환시 콘텐츠 배치가 화면에 최적화되지 않음
           - 요소들이 화면 특정 부분에 편중되어 전체적인 균형이 맞지 않음
         * overlapping
           - ui 요소들이 서로 겹쳐 내용이나 기능을 가림
           - 스크롤 시 고정 요소와 콘텐츠가 부적절하게 겹침
           - 요소의 그림자나 효과가 다른 요소와 충돌함
         * design
           - 디자인 가이드라인(모서리 반경, 그림자 등)이 일관되게 적용되지 않음
           - 다른 기능 요소에 동일한 디자인 스타일이 적용됨
           - 앱 내 달력, 시간 아이콘이 현재 날짜, 시간과 매칭되지 않음
         * visibility
           - 텍스트와 배경 간 대비가 낮아 가독성이 떨어짐
           - 상호작용 가능한 요소가 시각적으로 명확히 구분되지 않음
           - 상태변화(활성/비활성)를 나타내는 시각적 차이가 불충분함
         * iconography
           - 아이콘 스타일(아웃라인/필드)이 혼합되어 사용됨
           - 동일 기능의 아이콘이 화면마다 다르게 표현됨
           - 테마가 일부 아이콘에만 적용됨
        """

    @staticmethod
    def relevant_issues_prompt(rows):
        prompt = f"""
        다음은 클러스터링을 통해 동일한 군집으로 분류된 이미지에 대한 라벨들입니다.
        {rows}
        라벨은 다음과 같이 구성됩니다. 
        
        클러스터아이디_패키지명_화면/모듈명_날짜_시간_앱명_세부기능_오브젝트_이슈분류_작성자
        
        예시
        cluster00_com.android.settings_SubSettings_20250509_075445_Advance features_Double press_checkbox_cut_Zany.png

        세부 구성요소
        * 패키지명 : 앱 고유 식별자
        * 화면/모듈명 : 실제 화면 또는 ui 모듈 이름
        * 날짜 : 스크린샷 생성일
        * 시간 : 스크린샷 생성시각
        * 앱명 : 이슈가 발생하는 앱 명
        * 세부기능 : 실제 기능명
        * 오브젝트 : 화면 내의 문제 발생 지점
        * 이슈 분류 : 어떤 이슈인지 설명
        * 작성자 : 작성자

        그렇지만 라벨이 정확하지 않아 모든 구성요소가 존재하지 않을 수 있습니다. 
        이러한 라벨들을 통해 해당 클러스터에 존재할 수 있는 라벨을 추론해주세요.
        앱명,세부기능,이슈를 모두 담은 이슈 분류를 반환해주세요. 영어로 통일해주세요.

        응답으로는 이슈 분류 목록을 배열로 반환해주세요.
        """
        return prompt
    
    @property
    def generate_description_prompt():
        return """
        다음은 클러스터링된 이미지의 대표 레이아웃과 이슈를 찾아낼 타겟 이미지입니다.
        타겟 이미지를 보고 이 이미지가 표시하고 있는 애플리케이션명을 먼저 추출하세요.
        

        클러스터링된 이미지들에서 나타난 주요 이슈는 다음과 같습니다.
        이슈는 visibility - cutoff - 나머지 순으로 중요합니다.
        이 이슈를 참고하여 타겟 이미지에서 나타나고 있는 세부기능이나 오브젝트의 이슈등을 찾아주세요.
        
        이슈를 찾는 순서는 다음과 같습니다. 
        1. 타겟 이미지와 대표 레이아웃을 비교하여 레이아웃 상의 다른 점이나 이슈를 찾습니다.
        2. 타겟 이미지 자체에서 나타나고 있는 이슈를 제공된 주요 이슈를 참고하여 찾습니다. 

        응답으로는 애플리케이션명, 세부기능, 오브젝트, 이슈를 배열로 반환해주세요.
        이슈가 크리티컬한 정도를 함께 디테일에 표시해 주세요.
        가장 크리티컬한 이슈 하나만 반환해주세요.

        이슈가 없다면 이슈 없음을 반환해주세요.

        """

